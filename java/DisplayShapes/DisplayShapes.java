// Display shapes with special characters

public class DisplayShapes{
    public static void main(String[] args){
        System.out.println("     =======");
        System.out.println("      //  \\\\ ");
        System.out.println("     //    \\\\ ");
        System.out.println("    //      \\\\ ");
        System.out.println("   //========\\\\ ");
        System.out.println("  //          \\\\ ");
        System.out.println(" //            \\\\");
        System.out.println("====           ====");
    }
}
